package lacosderepetocoes.Arrays.multidimensional;

public class Aula01IntrodArraysMultidimensional {
    public static void main(String[] args) {
       int [][] arrayMuti1 = new int[2][2];
       arrayMuti1 [0][0] = 22;
       arrayMuti1 [0][1] = 11;
       arrayMuti1 [1][0] = 44;
       arrayMuti1 [1][1] = 55;

        System.out.println(arrayMuti1[0][0]);
        System.out.println(arrayMuti1[0][1]);
        System.out.println(arrayMuti1[1][0]);
        System.out.println(arrayMuti1[1][1]);
    }
}
